@seq=<STDIN>;
chomp(@seq);
for($i=0;$i<=$#seq;$i++)
{
	if($seq[$i]=~/^>/)
	{
		$seqname=$seq[$i];
		next;
	}
	for($j=$i+1;$j<=$#seq;$j++)
	{
		if($seq[$j]=~/^>/)
		{
			$seqname=$seq[$j];
			$seq[$j]="";
			next;
		}
		if($seq[$j]=~/$seq[$i]/)
		{
			$seq[$i]="";
		}
		elsif($seq[$i]=~/$seq[$j]/)
		{
			$seq[$j]="";
		}

	}
}
for($i=1;$i<=$#seq;$i++)
{
	if(length($seq[$i])>0)
	{
		print "$seqname\n$seq[$i]\n";
	}
}
